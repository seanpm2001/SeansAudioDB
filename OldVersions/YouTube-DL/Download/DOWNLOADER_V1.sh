# Download the video downloader I use
# Ubuntu method (old)
def installUbuntuOldMethod() {
sudo apt-get youtube-dl
}
# Ubuntu method (new)
def installUbuntuNewMethod() {
sudo apt install youtube-dl
}
# Fedora method (old)
def installFedoraNewMethod() {
sudo dnf install youtube-dl
}
# Fedora method (new)
def installFedoraNewMethod() {
sudo yum install youtube-dl
}
